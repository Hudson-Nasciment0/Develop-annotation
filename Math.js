/*

Math -> Matemática = é uma biblioteca interna que resolve as contas para nós.
-pow(2,2) / potência / 2² = 4 / 2
-sqrt(25,5) / raiz / 25
-PI
-ceil / teto -> arredondar para cima
-floor / chão -> arredondar para baixo

- random() / número aleatório entre 0 e 1
*/

const result = Math.pow(5,3) // calculo de potencialização 2³= 8
console.log(result)

const resultTwoo = Math.sqrt(25,2)
console.log(resultTwoo)

const resultThree = Math.PI //Valor de PI
console.log(resultThree)

const resultCeil = Math.ceil(3.14) //Arredonda valor para cima
console.log(resultCeil)

const resultFloor = Math.floor(3,14) // Arredonda valor para baixo
console.log(resultFloor)

const resultRandom = Math.random() // Trás um número aleatorio entre 0 e 1
console.log(resultRandom)